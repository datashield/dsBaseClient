context("ds.meanByClass::smk::setup")

connect.studies.dataset.cnsim(list("LAB_TSC","LAB_HDL","GENDER","DIS_DIAB","PM_BMI_CATEGORICAL"))

test_that("setup", {
    ds_expect_variables(c("D"))
})

#
# Tests
#

context("ds.meanByClass::smk::calculate the mean proportion for LAB_HDL across gender categories")
res <- ds.meanByClass(x='D', outvar='LAB_HDL', covar='GENDER')
test_that("LAB_HDL_across_gender", {
    expect_equal(res[[1]], '4768')
    expect_equal(res[[2]], '1.51(0.44)')
    expect_equal(res[[4]], '1.63(0.39)')
})

context("ds.meanByClass::smk::LAB_HDL across GENDER categories where both vectors are 'loose'")
ds.assign("D$LAB_HDL", "ldl")
ds.assign("D$GENDER", "sex")
res <- ds.meanByClass(x='ldl~sex')
test_that("LAB_HDL_across_gender_loose", {
    expect_equal(res[[1]], '4768')
    expect_equal(res[[2]], '1.51(0.44)')
    expect_equal(res[[4]], '1.63(0.39)')
})

context("ds.meanByClass::smk::calculate the mean proportion for LAB_HDL across gender and bmi categories")
res <- ds.meanByClass(x='D', outvar=c('LAB_HDL','LAB_TSC'), covar=c('GENDER'))
test_that("LAB_HDL_across_gender_bmi", {
    expect_equal(res[[1]], '4768')
    expect_equal(res[[7]], '4611')
    expect_equal(res[[2]], '1.51(0.44)')
    expect_equal(res[[4]], '5.92(1.11)')
    expect_equal(res[[8]], '5.78(1.03)')
})

context("ds.meanByClass::smk::calculate the mean proportion for LAB_HDL across gender bmi and diabetes status categories")
res <- ds.meanByClass(x='D', outvar=c('LAB_HDL','LAB_TSC'), covar=c('GENDER','PM_BMI_CATEGORICAL','DIS_DIAB'))
test_that("LAB_HDL_across_gender_bmi_diabetes", {
    expect_equal(res[[1]], '1168')
    expect_equal(res[[7]], '12')
    expect_equal(res[[2]], '1.59(0.42)')
    expect_equal(res[[4]], '5.88(1.04)')
    expect_equal(res[[8]], 'NA(NA)')
    expect_equal(res[[12]], '5.88(1.08)')
})

context("ds.meanByClass::smk::calculate the mean proportion for LAB_HDL across gender categories for each study separately")
res <- ds.meanByClass(x='D', outvar='LAB_HDL', covar='GENDER', type='split')
test_that("LAB_HDL_across_gender_split", {
    expect_equal(res$sim1[[1]], '1092')
    expect_equal(res$sim1[[4]], '1.62(0.39)')
    expect_equal(res$sim2[[2]], '1.5(0.42)')
    expect_equal(res$sim2[[3]], '1503')
    expect_equal(res$sim3[[1]], '2091')
    expect_equal(res$sim3[[4]], '1.65(0.39)')
})

