 stvar.transmit<-NULL
 }
 else
 {
 stvar.transmit<-stvar
 }
 
 
 if(is.null(exclude))
 {
 exclude.transmit<-NULL
 }
 else
 {
  exclude.transmit<-paste0(as.character(exclude),collapse=",")
 }
 
 useNA.transmit<-useNA
 
 if(is.null(force.nfilter))
 {
 force.nfilter.transmit<-NULL
